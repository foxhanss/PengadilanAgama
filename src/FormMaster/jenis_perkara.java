    private void tampilDataKeTabel() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Perkara"); // Ganti sesuai nama kolom di database
    model.addColumn("Jenis Perkara");
    model.addColumn("Tanggal Input");
    model.addColumn("Keterangan");
    model.addColumn("Biaya");

    try {
        String sql = "SELECT * FROM jenis_perkara"; // Ganti nama tabel sesuai database kamu
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_perkara"), 
                rs.getString("jenis_perkara"), 
                rs.getString("tanggal"), 
                rs.getString("keterangan"),
                rs.getString("biaya")
            });
        }

        table_perkara.setModel(model); // Ganti dengan nama JTable kamu
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
    }
    }
    
    private void cariData() {
    String keyword = cari.getText();
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Perkara");
    model.addColumn("Jenis Perkara");
    model.addColumn("Tanggal Input");
    model.addColumn("Keterangan");
    model.addColumn("Biaya");
    try {   
        String sql = "SELECT * FROM jenis_perkara WHERE " +
                    "jenis_perkara LIKE ? OR " +
                    "tanggal LIKE ? OR " +
                    "keterangan LIKE ? OR" +
                    "biaya LIKE ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        for (int i = 1; i <=3; i++) {
                pst.setString(i, "%" + keyword + "%");
            }
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_perkara"),
                rs.getString("jenis_perkara"),
                rs.getString("tanggal"),
                rs.getString("keterangan"),
                rs.getString("biaya")
                    
            });
        }

        table_perkara.setModel(model); // set ulang ke tabel
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Pencarian gagal: " + e.getMessage());
    }
    }
    
    private void editData() {
    int selectedRow = table_perkara.getSelectedRow();

    if (selectedRow != -1) {
        String id = table_perkara.getValueAt(selectedRow, 0).toString();
        List<String> fields = new ArrayList<>();
        List<Object> values = new ArrayList<>();

        if (namaJP.getSelectedItem() != null) {
            fields.add("jenis_perkara=?");
            values.add(namaJP.getSelectedItem().toString());
        }
        if (!tanggal_input.getText().isEmpty()) {
            fields.add("tanggal=?");
            values.add(tanggal_input.getText());
        }
        if (!ket.getText().isEmpty()) {
            fields.add("keterangan=?");
            values.add(ket.getText());
        }
        if (!biaya.getText().isEmpty()) {
            fields.add("biaya=?");
            values.add(biaya.getText());
        }

        if (fields.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tidak ada data yang diubah.");
            return;
        }

        try {
            String sql = "UPDATE jenis_perkara SET " + String.join(", ", fields) + " WHERE id_perkara=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            for (int i = 0; i < values.size(); i++) {
                pst.setObject(i + 1, values.get(i));
            }
            pst.setString(values.size() + 1, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
            ClearData();
            tampilDataKeTabel();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal update data: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diedit.");
    }
    }
    
    private void hapusData() {
    int selectedRow = table_perkara.getSelectedRow(); // nama tabel kamu

    if (selectedRow != -1) {
        String id = table_perkara.getValueAt(selectedRow, 0).toString(); // ambil id dari kolom 0

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM jenis_perkara WHERE id_perkara=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                tampilDataKeTabel(); // refresh tabel
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus.");
    }
    }
    
