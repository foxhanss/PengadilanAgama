    private void tampilDataKeTabel() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama Panitera");
    model.addColumn("NIP");
    model.addColumn("Jabatan");
    model.addColumn("Tanggal Mulai Bertugas");

    try {
        String sql = "SELECT * FROM panitera";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_panitera"),
                rs.getString("nama_panitera"),
                rs.getString("nip"),
                rs.getString("jabatan"),
                rs.getDate("tgl_mulai_tugas")
            });
        }
        tabel_panitera.setModel(model);
    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
    }
    }
    
    
    private void cariData() {
    String keyword = cari.getText();
    
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama Panitera");
    model.addColumn("NIP");
    model.addColumn("Jabatan");
    model.addColumn("Tanggal Mulai Bertugas");

    try {
        String sql = "SELECT * FROM panitera WHERE nama_panitera LIKE ? OR nip LIKE ? OR jabatan LIKE ? OR tgl_mulai_tugas LIKE ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        for (int i = 1; i <= 4; i++) {
                pst.setString(i, "%" + keyword + "%");
            }


        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_panitera"),
                rs.getString("nama_panitera"),
                rs.getString("nip"),
                rs.getString("jabatan"),
                rs.getDate("tgl_mulai_tugas")
            });
        }
        tabel_panitera.setModel(model);
    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pencarian Gagal: " + e.getMessage());
    }
}

    private void hapusData() {
    int selectedRow = tabel_panitera.getSelectedRow();

    if (selectedRow != -1) {
        String id = tabel_panitera.getValueAt(selectedRow, 0).toString();

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM panitera WHERE id_panitera=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                tampilDataKeTabel();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.");
    }
}
    
    private void editData() {
    int selectedRow = tabel_panitera.getSelectedRow();

    if (selectedRow != -1) {
        String id = tabel_panitera.getValueAt(selectedRow, 0).toString();

        String nama = nama_panitera.getText();
        String NIP = nip.getText();
        String Jabatan = jabatan.getText();
        Date tanggal = tgl_mulai_tugas.getDate(); // Date dari JDateChooser

        List<String> fields = new ArrayList<>();
        List<Object> values = new ArrayList<>();

        if (!nama.isEmpty()) {
            fields.add("nama_panitera=?");
            values.add(nama);
        }
        if (!NIP.isEmpty()) {
            fields.add("nip=?");
            values.add(NIP);
        }
        if (!Jabatan.isEmpty()) {
            fields.add("jabatan=?");
            values.add(Jabatan);
        }

        // ðŸŸ¢ Gunakan format tanggal yyyy-MM-dd jika tanggal tidak null
        if (tanggal != null) {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            fields.add("tgl_mulai_tugas=?");
            values.add(sdf.format(tanggal)); // hasil format jadi String
        }

        if (fields.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan isi minimal satu kolom untuk diperbarui.");
            return;
        }

        try {
            String sql = "UPDATE panitera SET " + String.join(", ", fields) + " WHERE id_panitera=?";
            PreparedStatement pst = conn.prepareStatement(sql);

            for (int i = 0; i < values.size(); i++) {
                pst.setObject(i + 1, values.get(i));
            }

            pst.setString(values.size() + 1, id); // ID ditaruh terakhir

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
            ClearData();
            tampilDataKeTabel();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal update data: " + e.getMessage());
        }

    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diedit.");
    }
}

    
